package Parser;

public class Operator implements Flang {

	Flang left ;
	Flang right ;
	
	public Flang getLeft() {
		return left;
	}

	public void setLeft(Flang left) {
		this.left = left;
	}

	public Flang getRight() {
		return right;
	}

	public void setRight(Flang right) {
		this.right = right;
	}
	
	
}
